-- ── medication_logs: already has patient_id (text) — normalize to uuid ───────
-- Existing app used text values like 'default'. Backfill before type change.

update public.medication_logs
set patient_id = '00000000-0000-4000-8000-000000000002'
where patient_id is null
   or patient_id = ''
   or patient_id = 'default';

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'medication_logs'
      and column_name = 'patient_id'
      and data_type <> 'uuid'
  ) then
    alter table public.medication_logs
      alter column patient_id type uuid
      using patient_id::uuid;
  end if;
exception
  when others then
    raise notice 'Could not alter medication_logs.patient_id to uuid automatically: %', sqlerrm;
    raise notice 'If patient_id is already uuid, you can ignore this.';
end $$;

alter table public.medication_logs
  drop constraint if exists medication_logs_patient_id_fkey;

alter table public.medication_logs
  add constraint medication_logs_patient_id_fkey
  foreign key (patient_id) references public.patients (id) on delete cascade;

update public.medication_logs
set patient_id = '00000000-0000-4000-8000-000000000002'::uuid
where patient_id is null;

-- ── RLS (match existing open policies used in medication_logs.sql) ──────────
alter table public.accounts enable row level security;
alter table public.patients enable row level security;
