-- NOTE: There is no login/auth table in the app today. We introduce a minimal
-- `accounts` row as the family/home tenant. The Streamlit app uses
-- CARESHIELD_ACCOUNT_ID (optional env var) matching the UUID below.

create extension if not exists "pgcrypto";

-- ── Fixed IDs for the default demo family (override via env in app if needed) ──
-- Default account: 00000000-0000-4000-8000-000000000001
-- Default patient: 00000000-0000-4000-8000-000000000002

create table if not exists public.accounts (
  id uuid primary key default gen_random_uuid(),
  name text not null default 'CareShield family account',
  created_at timestamptz not null default now()
);

insert into public.accounts (id, name)
values ('00000000-0000-4000-8000-000000000001'::uuid, 'CareShield family account')
on conflict (id) do nothing;

create table if not exists public.patients (
  id uuid primary key default gen_random_uuid(),
  account_id uuid not null references public.accounts (id) on delete cascade,
  name text not null,
  created_at timestamptz not null default now()
);

create index if not exists patients_account_id_idx
  on public.patients (account_id);

insert into public.patients (id, account_id, name)
values (
  '00000000-0000-4000-8000-000000000002'::uuid,
  '00000000-0000-4000-8000-000000000001'::uuid,
  'Patient'
)
on conflict (id) do nothing;

-- Conditions were session-only in the app; persist them per patient going forward.
create table if not exists public.patient_conditions (
  id bigint generated by default as identity primary key,
  patient_id uuid not null references public.patients (id) on delete cascade,
  name text not null,
  since text not null default '',
  badge text not null default 'chronic',
  created_at timestamptz not null default now()
);

create index if not exists patient_conditions_patient_id_idx
  on public.patient_conditions (patient_id);

